<p style="font-size:20px;color:black;text-align: justify;padding:10px;">Migraines follow a specific pattern of symptoms because of the way it affects the body. At the very beginning is a stimulus or triggering agent that causes a section of an artery of the brain to close down, to constrict. This prevents oxygenated blood from passing through that area of the brain.</p>
<p style="font-size:20px;color:black;text-align: justify;padding:10px;">The lack of oxygen causes the brain to malfunction resulting in the “prodramata”, the prodromal symptoms of nausea, visual disturbances, speech disorders, sweating, and numbness, tingling of the face, weakness in an arm or leg, or other signs.</p>
<p style="font-size:20px;color:black;text-align: justify;padding:10px;">Although these symptoms are alarming to some people, they are transient and there is no headache during this phase.</p>
<p style="font-size:20px;color:black;text-align: justify;padding:10px;">The body is programmed to not tolerate a constricted artery and responds by cutting off the nerve supply to the artery muscles that are locked into the constriction.</p>
<p style="font-size:20px;color:black;text-align: justify;padding:10px;">This allows the artery to open up again and resume circulation with the needed oxygen to the affected area of the brain. Unfortunately, the body over-reacts, allowing the artery to over-dilate.</p>
<p style="font-size:20px;color:black;text-align: justify;padding:10px;">Now the artery is ballooning out and stretching the muscles, the inner and outer walls of the artery beyond their normal elastic shape.</p>
<p style="font-size:20px;color:black;text-align: justify;padding:10px;">These are pain-sensitive tissues. The pain is severe when the artery over-dilates. As the heart pumps more blood through the artery, the pulsating pain just throbs and throbs.</p>
<p style="font-size:20px;color:black;text-align: justify;padding:10px;">Only once the nerve supply to the arterial muscles has been re-established, will the artery resume its normal shape and diameter. The pain subsides as the artery begins to heal from the traumatic episode.</p>
